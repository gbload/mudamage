	/**
	 * 部位名を返します
	 * @param part
	 * @param type
	 * @return part_name
	 */
	private static String getPartName(int part,double type){
		if(part==0){// 兜
			if(type==0)
				return "ヘルム";
			else if(type==1)
				return "兜";
			else if(type==2)
				return "マスク";
			else
				return "仮面";
		}else if(part==1){// 鎧
			if(type==0||type==2)
				return "アーマー";
			else
				return "鎧";
		}else if(part==2){// 手
			return "グラブ";
		}else if(part==3){// 腰
			return "ガーダー";
		}else{// 足
			if(type==0||type==2)
				return "ブーツ";
			else
				return "靴";
		}
	}
	/**
	 * 装備の名前の配列を返します。
	 * @param part 0:兜 1:鎧 2:手 3:腰 4:足
	 * @param job
	 * @return names
	 */
	public static String[] getNames(int part,Integer job){
		String[] str = new String[data.length];
		int size = 0;
		int jobnum = (1<<job);
		for(int i=0;i<data.length;i++)
			if((new Double(data[i][0][1]).intValue()&jobnum)!=0)
				str[size++] = series[i] + getPartName(part,data[i][0][0]);
		String[] names = new String[size];
		for(int i=0;i<size;i++){
			names[i] = str[i];
		}
		return names;
	}
